    <h2>About us</h2>
    <p>CAGEFIE is a cooperative society created on April 6, 2018, registered under number 18/057/CMR/CE/02/2008/CCA/361/3613. It is classified as a first-category microfinance institution and is headquartered in Messa plateau, Yaoundé.</p>
